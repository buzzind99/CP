package main

import (
	"bufio"
	"fmt"
	"os"
	"strconv"
)

var sc = bufio.NewScanner(os.Stdin)

func next() string {
	sc.Scan()
	return sc.Text()
}

func nextInt() int {
	i, _ := strconv.Atoi(next())
	return i
}

func main() {
	sc.Split(bufio.ScanWords)

	t := nextInt()
    for range t {
        s := next()
        if s[0] == 'a' || s[1] == 'b' || s[2] == 'c' {
            fmt.Println("YES")
        } else {
            fmt.Println("NO")
        }
    }
}


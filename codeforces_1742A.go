package main

import (
	"bufio"
	"fmt"
	"os"
	"strconv"
)

var sc = bufio.NewScanner(os.Stdin)

func next() string {
	sc.Scan()
	return sc.Text()
}

func nextInt() int {
	i, _ := strconv.Atoi(next())
	return i
}

func main() {
	sc.Split(bufio.ScanWords)

	t := nextInt()
    for range t {
        a, b, c := nextInt(), nextInt(), nextInt()
        if a == b+c || b == a+c || c == a+b {
            fmt.Println("YES")
        } else {
            fmt.Println("NO")
        }
    }
}

